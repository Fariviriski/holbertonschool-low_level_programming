#include "main.h"
/*
 * _strcmp - compares two strings
 * @index: index of a string
 * Return: 0 if equal, -1 is=f lesser, 1 if greater
 */

int _strcmp(char *s1, char *s2)
{
	int index;

	while (index[s1] == index[s2])
	{
		return (0);
	}

	if (index[s1] < index[s2])
	{
		return (-1);
	}
	else
	{
		return (1);
	}
}
